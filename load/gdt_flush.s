.global gdt_flush_phys

gdt_flush_phys:
    movl 4(%esp), %eax
    lgdt (%eax)

    # Load the ring-0 segments into the segment registers.
    mov $0x10, %ax
    mov %ax, %ds
    mov %ax, %es
    mov %ax, %fs
    mov %ax, %gs
    mov %ax, %ss
    ljmp $0x08, $.flush
.flush:
    ret
